public class File {

    int fileNO;
    String fileName;

    public int getFileNO() {
        return fileNO;
    }

    public void setFileNO(int fileNO) {
        this.fileNO = fileNO;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
}
